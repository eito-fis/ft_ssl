#include "libft.h"

void	ft_reverse(char *str)
{
	char *t;

	t = &(str[ft_strlen(str) - 1]);
	while (str < t)
	{
		*str ^= *t;
		*t ^= *str;
		*str ^= *t;
		str++;
		t--;
	}
}
