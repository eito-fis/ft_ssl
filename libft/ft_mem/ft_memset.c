#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*tmp;
	size_t	i;

	i = 0;
	tmp = (char*)str;
	while (i < n)
	{
		tmp[i] = c;
		i++;
	}
	return (str);
}
