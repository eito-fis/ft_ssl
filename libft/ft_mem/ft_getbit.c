#include "libft.h"

int		ft_getbit(int x, void *mem, int type)
{
	uint8_t		*buf8;
	uint32_t	*buf32;
	int			ret;

	ret = 0;
	if (type == 8)
	{
		buf8 = mem;
		ret = ((buf8[x / 8] & (1 << (7 - x % 8))) > 0);
	}
	else if (type == 32)
	{
		buf32 = mem;
		ret = ((buf32[x / 32] & (1 << (31 - x % 32))) > 0);
	}
	return (ret);
}
